EOF

echo ""
echo "âœ… Chaos testing complete!"
echo "ðŸ“Š Report saved to: $OUTPUT_REPORT"
echo ""

# Display summary
echo "Summary:"
echo "--------"
grep -A 20 "## Summary" "$OUTPUT_REPORT" | head -n 15

exit 0
